    private void bt_seleccionarimagenMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_bt_seleccionarimagenMouseClicked

        try {
            JFileChooser fc = new JFileChooser();
            FileNameExtensionFilter filtro = new FileNameExtensionFilter("Imagenes", "png", "jpg", "jpeg", "gif");
            fc.setFileFilter(filtro);
            File archivo;
            int op = fc.showOpenDialog(this);
            if (op == JFileChooser.APPROVE_OPTION) {
                archivo = fc.getSelectedFile();
                Image img = Toolkit.getDefaultToolkit().createImage(archivo.getPath()).getScaledInstance(lb_fotopapeleta.getWidth(), lb_fotopapeleta.getHeight(), Image.SCALE_DEFAULT);
                this.lb_fotopapeleta.setIcon(new ImageIcon(img));
            }
        } catch (Exception e) {
        }

    }//GEN-LAST:event_bt_seleccionarimagenMouseClicked

